package offer1;

import java.util.*;

class MaxQueue {
    //声明
    private Queue<Integer> q;
    private Deque<Integer> d;

    //定义
    public MaxQueue() {
        q = new LinkedList<>();
        d = new LinkedList<>();//deque只需存放每一段递增数列的最大值
    }

    public int max_value() {
        if (d.isEmpty()) {
            return -1;
        }
        return d.peekFirst();
    }

    public void push_back(int value) {
        while (!d.isEmpty() && d.peekLast() < value) {
            d.pollLast();
        }
        d.offerLast(value);
        q.offer(value);
    }

    public int pop_front() {
        if (q.isEmpty()) {
            return -1;
        }
        int ans = q.poll();
        if (ans == d.peekFirst()) {
            d.pollFirst();
        }
        return ans;
    }
}

